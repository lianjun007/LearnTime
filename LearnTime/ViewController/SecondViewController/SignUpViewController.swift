            let indexGroup = DispatchGroup()
            
            _ = user.signUp { (result) in
                switch result {
                case .success:
                    indexGroup.enter()
                    let alert = UIAlertController(title: "Ê≥®ÂÜåÊàêÂäü", message: "\(username)\nÊ¨¢ËøéÂä†ÂÖ•ËÆ∫ÂùõÔºàLearnTimeÔºâÔºÅ", preferredStyle: .alert)
                    alert.addAction(UIAlertAction(title: "Â•ΩÁöÑ", style: .default, handler: nil))
                    self.present(alert, animated: true, completion: nil)
                    indexGroup.leave()
                    
                    indexGroup.notify(queue: .main) {
                        // Ê≥®ÂÜåÂÆåÁ´ãÂç≥ÁôªÈôÜ
                        _ = LCUser.logIn(username: username, password: password) { result in
                            print("nihao")
                            switch result {
                            case .success(object: let user):
                                NotificationCenter.default.post(name: accountStatusChangeNotification, object: nil)
                            case .failure(error: let error):
                                print(error)
                            }
                        }
                    }
                    
                    self.dismiss(animated: true, completion: nil)
                case .failure(error: let error):
